            <h2>¿Qué es OEE?</h2>
            <p>La Eficiencia General de los Equipos (OEE) es una métrica utilizada para medir la eficiencia productiva de un equipo o una línea de producción. Se calcula multiplicando tres factores: Disponibilidad, Rendimiento y Calidad.</p>
        </section>
        <section>
            <h2>Disponibilidad</h2>
            <p>La Disponibilidad mide el tiempo que el equipo está realmente operando en comparación con el tiempo programado para operar. Se calcula como el tiempo de operación dividido por el tiempo programado.</p>
        </section>
        <section>
            <h2>Rendimiento</h2>
            <p>El Rendimiento mide la velocidad a la que el equipo opera en comparación con su capacidad máxima. Se calcula como la producción real dividida por la producción teórica máxima.</p>
        </section>
        <section>
            <h2>Calidad</h2>
            <p>La Calidad mide la proporción de productos buenos producidos en comparación con el total de productos fabricados. Se calcula como el número de productos buenos divididos por el número total de productos.</p>
        </section>
